package EXAMEN2_PRACTICAS;

public class Pacientes {
private String nombre;
private int edad;
private int nivelGravedad;

public Pacientes(String nombre, int edad, int nivelGravedad) {
	super();
	this.nombre = nombre;
	this.edad = edad;
	this.nivelGravedad = nivelGravedad;
}

public String getNombre() {
	return nombre;
}

public int getEdad() {
	return edad;
}

public int getNivelGravedad() {
	return nivelGravedad;
}
  







}
